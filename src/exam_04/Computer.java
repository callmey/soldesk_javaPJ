package exam_04;

public class Computer {
	// 멤버 변수
	double monitor;
	String keyboard;
	double 본체;
	double 마우스;
	
	// 생성자
	Computer() {
		monitor = 24;
		keyboard = "querty";
		본체 = 20.3;
		마우스 = 3.5;
		
	}
	
	//생성자 오버로딩
	Computer(double m, double body, double mouth) {
		this();
		this.monitor = m;
		this.본체 = body;
		this.마우스 = mouth;
	}
	
	// 멤버 메소드 = 행위
	void 연산하기() {
		System.out.println("하나 둘 하나 둘");
	}
	
	void 전원켜기() {
		System.out.println("어서오세요");
	}
	
	void 전원끄기() {
		 System.out.println("종료합니다");
	}
	
	void 코딩하기() {
		System.out.println("아 오늘도 야근인가");
	}
	
	
	public static void main(String[] args) {
		Computer com = new Computer();
		System.out.println("com1");
		System.out.println(com.마우스);
		com.전원켜기();
		
		System.out.println("---------------------");
		System.out.println("com2");
		Computer com2 = new Computer(10,10.5,8.5);
		System.out.println(com2.monitor);
		System.out.println(com2.본체);
		System.out.println(com2.마우스);
		com2.코딩하기();
		
	}
}

